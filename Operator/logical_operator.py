"""
logical operators are used to combine the conditional statements
"""

s = "Nasim"
x = 10

# and operator (and)
if s=="Nasim" and x ==10: # Return True if all the statement is True
    print("YES")
else:
    print("NO")

# or operator (or)
if s=="Nasim" or x ==1: # Return True if one of the statement is True
    print("YES")
else:
    print("NO")


# not operator (not)
flag=False
if not flag: # Reverse the result
    print("YES")
else:
    print("NO")





