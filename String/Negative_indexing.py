

"""
In python, there are two types of indexing of the string.
 1. Normal indexing: string from the 0th index to the (n-1)ith index
 2. Negative indexing : string from the (-n) to the (-1)
"""

s = "Nasim"
print(s[-1],s[-2],s[-3],s[-4],s[-5]) # Negative indexing print the string from the end to start

print(s[-4:-2])