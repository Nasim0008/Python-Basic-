tp = (1,2,3,4,5,6) # tuple with int values
print(tp)
print(type(tp))

tp = ("a","b","c","d") # tuple with string values
print(tp)
print(type(tp))

tp = (False,True,False,True,True) # tuple with boolean values
print(tp)
print(type(tp))

tp = (1,2,3,"a","b","c",True, False) # tuple with int, string, boolean values
print(tp)
print(type(tp))