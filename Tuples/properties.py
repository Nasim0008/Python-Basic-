tp=(1,2,3,4,5,6)

# tuple items are ordered, unchangeable,allow duplicate value

# Ordered:
"""
Tuples item are ordered, it means that all the tuples item are order,and this order will not changed
"""
print(tp[0],tp[3])

#Unchangeable:
#Tuples are unchangeable, meaning that we cannot change, add or remove items after the tuple has been created.


# Allow Duplicates:
"""
Since tuples are indexed, they can have items with the same value
"""

tp = ("a","b","c","a","b")
print(tp[0],tp[3])