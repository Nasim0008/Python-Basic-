tp  = ("one","two","three","four","five","six","seven")

print(tp) # print all the item of tp

print(tp[:]) # print all the item of tp

print(tp[2:5]) #print 2nd index item to the 4th index item (5th index is not included)

print(tp[3:]) # print from 3rd index item to the last index item

print(tp[:5]) # print from the first item to the 5th index item
