"""
Negative indexing means start from the end
-1 refers to the last index,
-2 refers to the second last index
........
......
-n refers to the first index
"""

tp = ("one","two","three","four","five")
n = len(tp)

print(tp[-1]) # last index item
print(tp[-2]) # second last index item
print(tp[-n]) # first index item

